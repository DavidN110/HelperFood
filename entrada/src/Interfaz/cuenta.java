    private void loginlabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginlabelMouseClicked
        contrasena = String.valueOf(Contrasena.getPassword());
        contacto = Contacto.getText();
        ubicacion = Ubicacion.getText();
        if (!ID.getText().equals("Ingrese el número de identificación sin puntos ni comas") && !ID.getText().isEmpty()){
            if (!contrasena.equals("********") && !contrasena.isEmpty()){
                if (!contacto.equals("Ingrese número de contacto o alguna dirección") && !contacto.isEmpty()){
                    if (!ubicacion.equals("Ingrese su ubicación") && !ubicacion.isEmpty()){
                        if(Ejecutable.getListaBeneficiarios().containsKey(Long.parseLong(ID.getText())) == false && Ejecutable.getListaDonantes().containsKey(Long.parseLong(ID.getText())) == false){
                            Ejecutable.crearUsuario(Long.parseLong(ID.getText()), contrasena, contacto, ubicacion, tipoUsuario);
                            principal paginaPrincipal = new principal();
                            paginaPrincipal.setVisible(true);
                            dispose();
                        }else{
                            if(Ejecutable.getBeneficiarioActivo() == null && Ejecutable.getDonanteActivo() == null){
                                JOptionPane.showMessageDialog(null, "El nombre de usuario ya se ha usado");  
                            }  
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "El campo de ubicación no se ha llenado");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "El campo de contacto no se ha llenado");
                }
            }else{
                JOptionPane.showMessageDialog(null, "El campo de contraseña no se ha llenado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "El campo de nombre no se ha llenado");
        }
        if (Ejecutable.getBeneficiarioActivo() != null || Ejecutable.getDonanteActivo() != null){
            Ejecutable.editarUsuario(Long.parseLong(ID.getText()), contrasena, contacto, ubicacion, tipoUsuario);
            JOptionPane.showMessageDialog(null, "La contraseña se ha guardado con éxito");
            principal paginaPrincipal = new principal();
            paginaPrincipal.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_loginlabelMouseClicked

