    private void UbicacionMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UbicacionMousePressed
        if(ID.getText().isEmpty()){
            ID.setText("Ingrese el número de identificación sin puntos ni comas");
            ID.setForeground(Color.gray);
        }
        if(String.valueOf(Contrasena.getPassword()).isEmpty()){
            Contrasena.setText("********");
            Contrasena.setForeground(Color.gray);
        }
        if(Contacto.getText().isEmpty()){
            Contacto.setText("Ingrese número de contacto o alguna dirección");
            Contacto.setForeground(Color.gray);
        }
        if(Ubicacion.getText().equals("Ingrese su ubicación")){
            Ubicacion.setText("");
            Ubicacion.setForeground(Color.black);
        }
    }//GEN-LAST:event_UbicacionMousePressed

